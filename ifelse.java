
import java.util.Scanner;

public class ifelse {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        if (a%2==0) {
            System.err.println("No is even");
        } else {
            System.err.println("No is ODD");
        }
    }
    
}
